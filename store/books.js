export const getters = {
  getBooksPreview(state) {
    return page => {
      page = page || 1
      return state.booksPreview[parseInt(page) - 1]
    }
  },
  getBook(state) {
    return bookid => {
      if (bookid) {
        return state.book
      }
      return state.newBook
    }
  },
  getPage(state) {
    return (pageid, editExistingPage) => {
      if (!editExistingPage) {
        return state.newPage
      }
      return state.book.content[pageid - 1]
    }
  }
}
